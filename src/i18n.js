/* =========================================================================
   i18n.js — UI/코드내 문구 다국어 조회 (영문을 키로)
   ---------------------------------------------------------------------------
   JSX 의  lang === "ko" ? <ko> : <en>  하드코딩을  t(<en>, lang) 으로 대체.
   번역본은 src/i18n-ui.json ({ "<영문>": { en, ko } }) 에서 관리(엑셀 왕복).
   맵에 없거나 값이 비면 영문(키) 폴백 → 항상 안전.
   ========================================================================= */
import UI from "./i18n-ui.json" with { type: "json" };

export function t(en, lang) {
  const m = UI[en];
  const v = m && m[lang];               // en | ko | ar
  return v || (m && m.en) || en;        // 미번역 언어는 영문 폴백
}
